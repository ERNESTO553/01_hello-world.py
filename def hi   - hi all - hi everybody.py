def hi (name):
    print ("Hi, ", name)
hi ("Greg")

####
def hiAll(name1, name2):
    print("HI, ",name1)
    print("HI, ",name2)
hiAll("Daniel","Sebastian")

#####
def hiEverybody (myList):
    for name in myList:
        print("Hi,", name)
hiEverybody(["Adam", "Jhon", "Lucy"])
    
